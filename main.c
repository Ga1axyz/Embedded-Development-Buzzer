int main( void )
{
 	u8 p, m, tem, pos; 					// m为节拍
 	u8 i = 0, key = 0,flag = 0;
	Stm32_Clock_Init( 6 );  				// 6倍频
	delay_init( 72 ); 					// 12M外部晶振
	KeyInit();
	BzInit();
	Timer3Init( 71 ); 					// 1M
	LED_Init();
	LED_SEL = 0;
	
	while( 1 )
	{
		if(flag == 0)
			p = music1[i];
		if(flag == 1)
			p = music2[i];
		if(flag == 2)
			p = music3[i];
		if(flag == 3)
			p = music4[i];
		
		if( p==0xf1 )
		{
			flag = 1;
			i = 0;
			continue;
		}
		else if( p==0xf2 )
		{
			flag = 2;
			i = 0;
			continue;
		}
		else if( p==0xf3 )
		{
			flag = 3;
			i = 0;
			continue;
		}
		
		//p = music[i]; 				// 如果碰到结束符，延时1秒,回到开始再来一遍 
		if( p==0x00 )
		{
			i = 0;
			TIM3->CR1&= ~(0x01);    		//关闭定时器3
		}
		else if( p==0xff ) 				// 若碰到休止符,延时100ms,继续取下一音符
		{
			i = i + 2;
			delay_ms(100);
			TIM3->CR1&= ~(0x01);    		// 关闭定时器3
		}
		else 						// 正常情况下取音符和节拍 
		{
			if(p != 0x88){
				if(flag == 0)
					tem = QuYin( music1[i] );
				if(flag == 1)
					tem = QuYin( music2[i] );
				if(flag == 2)
					tem = QuYin( music3[i] );
				if(flag == 3)
					tem = QuYin( music4[i] );
				//tem = QuYin( music[i] ); 	// 取出当前音符在quzi数组中的位置值
				TIM3->ARR = 0xffff-quzi[tem]; 	
			}
			else{
				TIM3->ARR = 0;
			}
			i++;
			if(flag == 0)
				m = music1[i];
			if(flag == 1)
				m = music2[i];
			if(flag == 2)
				m = music3[i];
			if(flag == 3)
				m = music4[i];
			//m = music[i]; 			// 取得节拍
			i++;
			delay_ms(62);
		}
		TIM3->CR1 |= 0x01;    				// 使能定时器3
		
		pos = p;
		p &= 0xf;
		pos /= 16;
		
		
		//SetLed(0,p);					// LED 闪烁相应的简谱
		SetLed(pos,p);					// 数码管 闪烁相应的简谱
		
		delay_ms(m*24);
		TIM3->CR1 &=  ~(0x01);    //关闭定时器3
	}
}


